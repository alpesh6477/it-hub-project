        <h3 class="n2"> Health And Safety Policy For Body Blast Gym</h3>
        <br>
        <h2 class="n3">Introduction</h2>
        <p class="n3">At <b>Body Blast Gym</b>, the health and safety of our members and staff are our top priorities. This policy outlines our commitment to providing a safe and healthy environment and the responsibilities of both staff and members in maintaining this standard.</p>
        <br>
        <h2 class="n3">Policy Statement</h2>

        <h5 class="n3">We are committed to:</h5>

        <ul class="n3">
            <li>Providing safe and well-maintained equipment.</li>
            <li>Ensuring clean and hygienic facilities.</li>
            <li>Complying with all relevant health and safety legislation.</li>
        </ul>
        <br>
        <h2 class="n3">Responsibilities</h2>
        <h5 class="n3">Management Responsibilities:</h5>

        <ul class="n3">
            <li>Regularly reviewing and updating health and safety protocols.</li>
            <li>Providing training to staff on health and safety practices.</li>
            <li>Ensuring emergency procedures are in place and well communicated.</li>
        </ul>
        <br>
        <h2 class="n3"> Staff Responsibilities</h2>


        <ul class="n3">
            <li>Following all health and safety guidelines.</li>
            <li>Reporting any hazards or incidents to management promptly.</li>
            <li>Assisting members with proper use of equipment.</li>
        </ul>
        <br>
        <h2 class="n3"> Member Responsibilities</h2>


        <ul class="n3">
            <li>Using equipment safely and as intended.</li>
            <li>Reporting any unsafe conditions or equipment to staff.</li>
            <li>Adhering to gym rules and guidelines.</li>
        </ul>
        <br>
        <h2 class="n3"> Health and Safety Practices</h2>

        <ul class="n3">
            <li><b>Equipment Safety:</b> Regular inspections and maintenance of gym equipment.</li>
            <li><b>Cleanliness:</b> Regular cleaning and sanitization of the gym area, especially high-touch surfaces.</li>
            <li><b>Physical Distancing:</b> Adhering to physical distancing guidelines as applicable.</li>
            <li><b>First Aid:</b> Availability of a well-stocked first aid kit and trained personnel on-site.</li>
            <li><b>Emergency Procedures:</b> Clearly marked exits, fire extinguishers, and evacuation plans in place.</li>

        </ul>
        <br>
        <h2 class="n3">Incident Reporting</h2>
        <p class="n3">Members and staff are required to report any accidents, injuries, or health emergencies immediately to the gym staff. An incident report will be filed, and necessary actions will be taken to address the issue.</p>
        <br>
        <h2 class="n3">Policy Review and Updates</h2>
        <p class="n3">This policy will be reviewed annually and updated as necessary to ensure the highest standards of health and safety are maintained.</p>
